                               <h3>The idea</h3>

                                         <p>Please forget all of the terrible deluded nonsense you’ve heard about the value of ideas. Ideas are cheap, fleeting things; by itself an idea is worth less than a half-eaten sandwich. At least you can eat the sandwich.

                                        You do need an idea of course. But understand that even the most successful companies were not founded on wild or brilliant ideas. Starbucks chose the brazen path of selling coffee in Seattle. Facebook built a better MySpace. Google built a better Yahoo search. Microsoft copied Apple – who copied Xerox.

                                        Original ideas are overrated. What isn’t overrated is timing. Google chose the perfect time to build a better search engine – good luck trying to do that now *cough* Bing *cough*. What you want, therefore, is an astute awareness of a need that is currently underrepresented in the market. You want to spot a product or service that can go places – original or not. It’s usually easier to refine an existing idea that isn’t fully realised than to create a wholly original one.

                                        People fear setting up a business wherever there’s competition, but competition can be a good thing. The best place to set up a new restaurant is right next to another successful restaurant; they’ve kindly done the hard work for you of building an audience. Many a good business has ridden to success on the coattails of another – it is usually better to have some rivals over none. You just need to become 10% better.

                                        I personally recommend trying to deliver something that you and your friends would buy in a heartbeat. You’ll know more about your field, you’ll understand your customers, and you’ll be passionate about what you do. If you can make your company about a why – not a what – you’ll inspire yourself and those around you. And to survive the next step, you need a fair sprinkle of inspiration:</p>
                                        <h3>Starting</h3>

                                        <p>Starting a company is a bit like parenting; everyone assumes you know what you’re doing, but babies and companies don’t come with instruction manuals. You stumble through it, learning as you go.

                                        It’s at the start where you’re most likely to fail. Your aim is to build that magical money-making machine, but you probably don’t have all the parts and the ones that you need may cost more than you have. Your idea is probably at least half wrong too, but you won’t know which half yet. All of this is normal.</p>
